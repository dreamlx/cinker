package com.cinker.model;

public class ConcessionTabs {
	
	private ConcessionItems[] concessionItems;
	private int id;
	private String name;
	
	public ConcessionItems[] getConcessionItems() {
		return concessionItems;
	}
	public void setConcessionItems(ConcessionItems[] concessionItems) {
		this.concessionItems = concessionItems;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
